clear;
clc;

hold on

%--------------------------------------------------------------------------

theta = 55*pi/180;
t = tan(theta);

X=[0,0,1/t];
Y=[-1,0,0];

x=X(1:2);
y=Y(1:2);
plot(x,y,'k');

x=X(2:3);
y=Y(2:3);
plot(x,y,'k');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'k');

%--------------------------------------------------------------------------

X=[-1/t,1/t,1/t];
Y=[-1,1,-1];

x=X(1:2);
y=Y(1:2);
plot(x,y,'k');

x=X(2:3);
y=Y(2:3);
plot(x,y,'k');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'k');

%--------------------------------------------------------------------------

X=[0,1/(2*t),0];
Y=[0,-1/2,-1/2];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

X=[-1/(2*t),-1/(2*t),0];
Y=[-1/2,-1,-1/2];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

X=[1/(2*t),1/(2*t),1/t];
Y=[-1/2,-1,-1/2];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

hold off
axis equal
set(gca,'xlim',[-1/t-0.2 1/t+0.2]);
set(gca,'ylim',[-1.2 1.2]);
set(gca,'Box','off');
set(gca,'xtick',[]);
set(gca,'ytick',[]);