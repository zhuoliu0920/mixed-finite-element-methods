clear;
clc;

hold on

%{
ang=0:0.01:2*pi;
x = cos(ang);
y = sin(ang);
plot(x,y,'r');
%}

t = (1+sqrt(5))/2;
s = t/sqrt(1+t*t);
c = 1/sqrt(1+t*t);
theta = atan(t);

%--------------------------------------------------------------------------

X=[-2,0,2];
Y=[-t,t,-t];

x=X(1:2);
y=Y(1:2);
plot(x,y,'k');

x=X(2:3);
y=Y(2:3);
plot(x,y,'k');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'k');

%--------------------------------------------------------------------------

X=[-1,0,1];
Y=[0,-t,0];

x=X(1:2);
y=Y(1:2);
plot(x,y,'k');

x=X(2:3);
y=Y(2:3);
plot(x,y,'k');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'k');

%--------------------------------------------------------------------------

X=[-1,0,1];
Y=[0,-1,0];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

X=[-s*sin(theta-pi/4)*sqrt(2),0,s*sin(theta-pi/4)*sqrt(2)];
Y=[-1-c*sin(theta-pi/4)*sqrt(2),-1,-1-c*sin(theta-pi/4)*sqrt(2)];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

x=[0,0];
y=[-1,-t];
plot(x,y,'--');

%--------------------------------------------------------------------------

X=[-s*sin(theta-pi/4)*sqrt(2),-1,-2+s*sin(theta-pi/4)*sqrt(2)];
Y=[-1-c*sin(theta-pi/4)*sqrt(2),-t,-1-c*sin(theta-pi/4)*sqrt(2)];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

x=[-1,-1];
y=[0,-t];
plot(x,y,'--');

%--------------------------------------------------------------------------

X=[s*sin(theta-pi/4)*sqrt(2),1,2-s*sin(theta-pi/4)*sqrt(2)];
Y=[-1-c*sin(theta-pi/4)*sqrt(2),-t,-1-c*sin(theta-pi/4)*sqrt(2)];

x=X(1:2);
y=Y(1:2);
plot(x,y,'--');

x=X(2:3);
y=Y(2:3);
plot(x,y,'--');

x=X(1:2:3);
y=Y(1:2:3);
plot(x,y,'--');

%--------------------------------------------------------------------------

x=[1,1];
y=[0,-t];
plot(x,y,'--');

%--------------------------------------------------------------------------



hold off
axis equal
set(gca,'xlim',[-2.2 2.2]);
set(gca,'ylim',[-2 2]);
set(gca,'Box','off');
set(gca,'xtick',[]);
set(gca,'ytick',[]);